// Boolean operation nodes
CSG.NodeLeaf = class extends CSG.Node
{
    /* =====================================================================================================
     *  CONSTRUCTOR
     * ===================================================================================================== */
    constructor (parent = null, solid = null) {
        super(parent);
        this._solid  = solid;
    }

    /* =====================================================================================================
     *  GETTERS & SETTERS
     * ===================================================================================================== */
    get solid  () { return this._solid;  }

    set solid  (solid)  { this._solid  = solid;  }

    /* =====================================================================================================
     *  Methods
     * ===================================================================================================== */
    geometry () {
        return this._solid.geometry();
    }
};